import { useState } from 'react';
import { Gauge, ArrowLeft, Loader2, CheckCircle, AlertCircle, Download, Copy } from 'lucide-react';
import Link from 'next/link';
import FileUpload from '@/components/file-upload';
import { downloadJmx, downloadScala } from '@/lib/export-utils';

